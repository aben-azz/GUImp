#include "libft.h"

int	ft_putnbr(int n)
{
	char c;

	if (n < 0)
	{
		write(1, "-", 1);
		if (n == -2147483648)
		{
			write(1, "2", 1);
			n = -147483648;
		}
		n = n * -1;
	}
	if (n > 9)
		ft_putnbr(n / 10);
	c = 48 + n % 10;
	write(1, &c, 1);
	return (1);
}
